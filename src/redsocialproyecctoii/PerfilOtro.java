    private void SeguirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_SeguirMouseClicked
        boolean YaLoSigo=false;
        
        for (int i = 0; i <mas.UsuarioActual.getNumFollowing(); i++) {
            if (mas.UsuarioBusqueda.getUser().equals(mas.UsuarioActual.getfollowing()[i])) {
                YaLoSigo=true;
                break;
            }
        }
                    
            
            if (YaLoSigo) {
                JOptionPane.showMessageDialog(null, "Ya sigues a @"+mas.UsuarioBusqueda.getUser());
                
            }else{
                 mas.agregarFollower();
                 JOptionPane.showMessageDialog(null, "Ahora sigues a @"+mas.UsuarioBusqueda.getUser());
                 lblFollowers.setText("Followers: "+mas.UsuarioBusqueda.getNumFollowers());
                 lblFollowing.setText("Following: "+mas.UsuarioBusqueda.getNumFollowing());

            }
    }//GEN-LAST:event_SeguirMouseClicked

