    private void DejardeSeguirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_DejardeSeguirMouseClicked
        boolean SigueUsuario=false;
        
        for (int i = 0; i < mas.UsuarioActual.getNumFollowing(); i++) {
            if (mas.UsuarioBusqueda.getUser().equals(mas.UsuarioActual.getfollowing()[i])) {
                SigueUsuario=true;
                break;
            }
        }
        
        if(SigueUsuario){
            mas.DejardeSeguir(); 
            JOptionPane.showMessageDialog(null, "Dejaste de Seguir a @"+mas.UsuarioBusqueda.getUser());
            lblFollowers.setText("Followers: "+mas.UsuarioBusqueda.getNumFollowers());
            lblFollowing.setText("Following: "+mas.UsuarioBusqueda.getNumFollowing());  
        }else{

            JOptionPane.showMessageDialog(null, "No sigues a @"+mas.UsuarioBusqueda.getUser());
        }
                
    }//GEN-LAST:event_DejardeSeguirMouseClicked

