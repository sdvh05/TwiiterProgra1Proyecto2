    private void logBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_logBtnActionPerformed
        String user=txtUser.getText();
        String password=txtPass.getText();
        
     
       if ( user.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor, rellenar todas las casillas");
        } else {
           boolean login=mas.Login(user, password);
            if (login) {
                JOptionPane.showMessageDialog(null, "SE HA INICIADO SESION CORRECTAMENTE");
                
                 new Navegacion(mas).setVisible(true);
                 setLocationRelativeTo(null);
                 this.dispose();

            } else {
                JOptionPane.showMessageDialog(null, "Usuario o ContraseÃ±a equivocado", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
        
        
    }//GEN-LAST:event_logBtnActionPerformed

