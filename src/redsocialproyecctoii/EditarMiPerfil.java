    private void EDITARMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_EDITARMouseClicked
        // TODO add your handling code here:
       String name= TxtNombre.getText();
       String user = TxtUser.getText();       
       String password = TxtPass.getText();
       int edad=(Integer) EdadSpin.getValue();
       String oldUser=mas.UsuarioActual.getUser();
       

        
        for (int i = 0; i < mas.NumUsers; i++) {
             if (user.equals(mas.users[i].getUser()) && !user.equals(mas.UsuarioActual.getUser())) {
                JOptionPane.showMessageDialog(null, "El Username: "+user+" ya Existe");
                return;
            }
        }

       
        int confirmar = JOptionPane.showConfirmDialog(null,"Â¿Desea Aplicar los cambios?", "Confirmacion", JOptionPane.OK_CANCEL_OPTION);
        if (confirmar==0) {
            for (int i = 0; i <mas.NumUsers; i++) {
                if (mas.users[i].equals(mas.UsuarioActual)) {
                    mas.users[i].setUser(user);
                }
            }
            mas.UsuarioActual.setNombre(name);
            mas.UsuarioActual.setUser(user);
            mas.UsuarioActual.setPass(password);
            mas.UsuarioActual.setEdad(edad);
            
          
            //Cambair lista Following de Otros
           for(Usuario u : mas.users){
               if(u==null){
                   break;
               }
               for ( String usr : mas.UsuarioActual.getfollowers()) {
                   if(u==null){
                       break;
                   }
                   if (u.getUser().equals(usr)) {
                       for (int i = 0; i < u.getNumFollowing(); i++) {
                           if (u.getfollowing()[i].equals(oldUser)) {
                               u.UpdateFollowing(user, i);
                           }
                       }
 
                   }
               }
               
           }
           
           //Cambair lista Followers de Otros
           for(Usuario u : mas.users){
               if(u==null){
                   break;
               }
               for ( String usr : mas.UsuarioActual.getfollowers()) {
                   if(u==null){
                       break;
                   }
                   if (u.getUser().equals(usr)) {
                       for (int i = 0; i < u.getNumFollowers(); i++) {
                           if (u.getfollowers()[i].equals(oldUser)) {
                               u.UpdateFollowers(user, i);
                           }
                       }
 
                   }
               }
               
           }
           
           
            
            for (int i = 0; i < mas.NumTweets; i++) {
                if (mas.tweets[i].getUsuario().equals(oldUser)) {
                    mas.tweets[i].setUsuario(user);
                }
            }
           
            MiPerfil mp =new MiPerfil(mas);
            mp.setVisible(true);
            this.dispose();
            
        }
       
    }//GEN-LAST:event_EDITARMouseClicked

